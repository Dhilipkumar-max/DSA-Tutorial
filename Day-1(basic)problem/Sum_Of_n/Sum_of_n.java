// Space Complexity: O(1)

// The formula for the sum of the first n natural numbers is n(n + 1) / 2. This allows us to calculate the sum in constant time, O(1), and constant space, O(1).   
public class Sum_of_n {
    public static int sumOfN(int n) {
        return n * (n + 1) / 2;
    }
    public static void main(String[] args) {
        int n = 10;
        System.out.println("Sum of first " + n + " natural numbers is: " + sumOfN(n));
    }
}